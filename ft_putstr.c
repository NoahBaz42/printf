#include "ft_printf.h"

int	ft_pustr(char *str)
{
	int i;
	
	if(!str)
		write(1, "(null)", 6);
	i = 0;
	while(str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
	return (i);
}